    public boolean NV_checkUpdate() {
        if (txtNV_ID.getText().isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập mã nhân viên trước khi lưu vào danh sách !.");
            txtNV_ID.requestFocus();
            return false;
        } else if (txtNV_diaChi.getText().isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập địa chỉ hiện tại nhân viên trước khi lưu");
            txtNV_diaChi.requestFocus();
            return false;
        }
        Pattern manv = Pattern.compile("^NV+[Z0-9$]");
        Matcher matchermanv = manv.matcher(txtNV_ID.getText());
        if (!matchermanv.find()) {
            MsgBox.alert(this, "Mã nhân viên không đúng định dạng!. Vui lòng nhập lại ");
            txtNV_ID.requestFocus();
            return false;
        }
        Pattern email = Pattern.compile("^[a-zA-Z0-9]\\w{2,}@\\w{2,}(.\\w{2,3}){1,2}$");
        Matcher matchermail = email.matcher(txtNV_Email.getText());
        if (!matchermail.find()) {
            MsgBox.alert(this, "Email không hợp lệ!. Vui lòng nhập lại!");
            txtNV_Email.requestFocus();
            return false;
        }
        Pattern sdt = Pattern.compile("(0[3|5|7|8|9])+([0-9]{8})");
        Matcher matchersdt = sdt.matcher(txtNV_SDT.getText());
        
        if (!matchersdt.find()) {
            MsgBox.alert(this, "Số điện thoại không hợp lệ ! ");
            txtNV_SDT.requestFocus();
            return false;
        }
        try {
            Date m = XDate.toDate(txtNV_ngaySinh.getText(), "dd/MM/yyyy");
            m.parse(txtNV_ngaySinh.getText());
        } catch (Exception e) {
            MsgBox.alert(this, "Vui lòng nhập ngày theo đúng định dạng [dd/MM/yyyy]  ");
            txtNV_ngaySinh.requestFocus();
            return false;
        }
        return true;
    }
    

    private void txtluong_maNVFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtluong_maNVFocusLost
        try {
            if (txtluong_maNV.getText().isEmpty()) {
                txtluong_tenNV.setText("");
            } else {
                String manv = txtluong_maNV.getText();
                nhanVien nv = dao.selectById(manv);
                txtluong_tenNV.setText(nv.getHoTen());
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Mã nhân viên này không tồn tại!");
        }

    }//GEN-LAST:event_txtluong_maNVFocusLost


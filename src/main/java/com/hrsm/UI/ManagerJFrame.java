    public void getfucntionChange() {
        String matKhau = new String(txtpresent_pass.getPassword());
        String matKhauMoi = new String(txtnew_pass.getPassword());
        String confirm = new String(txtconfirm_pass.getPassword());
        
        if (!matKhau.equals(Auth.user.getMatKhau())) {
            MsgBox.alert(this, "Sai mật khẩu!");
        } else if (!matKhauMoi.equals(confirm)) {
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng!");
        } else {
            Auth.user.setMatKhau(matKhauMoi);
            daoND.update(Auth.user);
            MsgBox.alert(this, "Đổi mật khẩu thành công!");
        }
    }
    

    public void CC_setForm(chamCong cc) {
        txtCC_tenNV.setText(cc.getTennv());
        txtCC_maNV.setText(cc.getManv());
        txtCC_ngayCC.setDate(cc.getNgaychamcong());
        txtCC_thoiGian.setText(String.valueOf(cc.getThoigianlam()));
        txtCC_gioTangCa.setText(String.valueOf(cc.getGiotangca()));
    }

    public void CC_fillTableAttendance() {
        DefaultTableModel model = (DefaultTableModel) tblCC_danhSach.getModel();
        model.setRowCount(0);
        try {
            String maNV = String.valueOf(Auth.user);
            List<chamCong> list = daoCC.selectByMaNV(maNV);
            for (chamCong cc : list) {
                Object[] records = {
                    cc.getManv(),
                    cc.getTennv(),
                    cc.getNgaychamcong(),
                    cc.getThoigianlam(),
                    cc.getGiotangca()
                };
                model.addRow(records);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    chamCong CC_getForm() {
        chamCong cc = new chamCong();
        cc.setManv(txtCC_maNV.getText());
        cc.setNgaychamcong(txtCC_ngayCC.getDate());
        cc.setGiotangca(Integer.parseInt(txtCC_gioTangCa.getText()));
        cc.setThoigianlam(Integer.parseInt(txtCC_thoiGian.getText()));
        return cc;
    }

    public void CC_loadForm() {
        String manv = (String) tblCC_danhSach.getValueAt(this.records_CC, 0);
        chamCong cc = daoCC.selectById(manv);
        this.CC_setForm(cc);
        tabchamCong.setSelectedIndex(0);
    }

    boolean CC_validate() {
        String v = String.valueOf(Auth.user);
        List<chamCong> list = daoCC.selectByMaNV(v);
        for (chamCong cc : list) {
            if (!cc.getManv().equalsIgnoreCase(txtCC_maNV.getText())) {
                MsgBox.alert(this, "Mã nhân viên không tồn tại");
                return false;
            } else if (cc.getNgaychamcong().equals(txtCC_ngayCC.getDate())) {
                MsgBox.alert(this, "Bạn đã chấm công vào ngày này");
                return false;
            }
        }
        return true;
    }

    public void CC_insert() {
        chamCong model = CC_getForm();
        try {
            daoCC.insert(model);
            this.CC_fillTableAttendance();
            MsgBox.alert(this, "Chấm Công Thành Công");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Chấm Công Thất Bại");
        }
    }

    public void CC_first() {
        this.records_CC = 0;
        CC_edit();
    }

    public void CC_prev() {
        if (this.records_CC > 0) {
            this.records_CC--;
            CC_edit();
        } else {
            this.CC_last();
        }
    }

    public void CC_next() {
        if (this.records_CC < tblCC_danhSach.getRowCount() - 1) {
            this.records_CC++;
            CC_edit();
        } else {
            this.CC_first();
        }
    }

    public void CC_last() {
        this.records_CC = tblCC_danhSach.getRowCount() - 1;
        CC_edit();
    }

    public void CC_edit() {
        String manv = (String) tblCC_danhSach.getValueAt(this.records_CC, 0);
        chamCong kl = daoCC.selectById(manv);
        this.CC_setForm(kl);
        tabchamCong.setSelectedIndex(0);
    }


    public void da_fillTableInformationProject() {
        DefaultTableModel model = (DefaultTableModel) tblDA_danhSach.getModel();
        model.setRowCount(0);
        try {
            String progress = (String) cbxDA_tienDo.getSelectedItem();
            String maNV = String.valueOf(Auth.user);
            int year = (Integer) cbxDA_nam.getSelectedItem();
            List<duAn> list = daoDA.selectByComponents(maNV, progress, year);
            for (duAn da : list) {
                Object[] records = {
                    da.getTenPB(),
                    da.getTenDA(),
                    da.getNgayBatDau(),
                    da.getNgayKetThuc(),
                    da.getTienDo()
                };
                model.addRow(records);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void da_fillComboBoxYears() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxDA_nam.getModel();
        model.removeAllElements();
        List<Integer> list = daoDA.selectYears();
        for (Integer year : list) {
            model.addElement(year);
        }
    }

    public void da_fillComboboxProgress() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxDA_tienDo.getModel();
        model.removeAllElements();
        List<String> list = daoDA.selectProgress();
        for (String item : list) {
            model.addElement(item);
        }
    }


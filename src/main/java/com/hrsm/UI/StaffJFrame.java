    public void Time() {
        Thread t1 = new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Date now = new Date();
                    SimpleDateFormat simpleDateFormat = new SimpleDateFormat();
                    simpleDateFormat.applyPattern("hh:mm:ss aa");
                    String txt = simpleDateFormat.format(now);
                    lbTime.setText(txt);
                }
            }
        });
        t1.start();
    }


    public void luong_fillTableSalary() {
        DefaultTableModel model = (DefaultTableModel) tblluong_danhSach.getModel();
        model.setRowCount(0);
        String maNV = String.valueOf(Auth.user);
        String progress = (String) cbxluong_trangThai.getSelectedItem();
        List<luong> list = daoL.selectByComponents(maNV, progress);
        for (luong l : list) {
            model.addRow(new Object[]{
                l.getMaNV(),
                l.getTenNV(),
                l.getThoiGianTinh(),
                l.getSoNgayLam(),
                l.getSoNgayNghi(),
                l.getSoNgayTangCa(),
                l.getLoaiViPham(),
                l.getTrachNhiem(),
                l.getThuong(),
                l.getKhautru(),
                l.getLuongCoBan(),
                l.getTrangThai() ? "Đã xử lí" : "Đang xử lí",
                l.getTong()
            });
        }
    }

    public void luong_fillComboboxProgress() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxluong_trangThai.getModel();
        model.removeAllElements();
        List<String> list = daoL.selectProgress();
        for (String item : list) {
            model.addElement(item);
        }
    }

